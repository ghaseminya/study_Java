/*
double형 변수의 선언과 활용

*/

public class VariableDouble01 {

	public static void main(String[] args)
	{
		//동일한 자료형으로 두개이상의 변수 선언
		double num1, num2, result;
		//변수 초기화
		num1=11.0000001;
		num2=22.0000002;
		//연산 수행후 결과 저장
		result=num1+num2;
		
		System.out.println(result);
	}

}
