
public class VariableBoolean01 {

	public static void main(String[] args) {
		
		//boolean자료형은 true/false만 저장가능
		boolean b1=true;
		boolean b2=false;
		
		System.out.println(b1);
		System.out.println(b2);
		
		//연산의 결과에 의한 true/false출력
		System.out.println(7<10);
		System.out.println(7>10);

	}
}
